    private void addTableColumn(){
        tableModel.addColumn("plate_nbr");
        tableModel.addColumn("bus_name");
        tableModel.addColumn("driver");
        tableModel.addColumn("company");
        tableModel.addColumn("nbr_passengers");
       
        busTable.setModel(tableModel);
     }
     public void populateTable(){
      tableModel.setRowCount(0);
         BusDAO busdao=new BusDAO();
     
        List<Bus> buses = busdao.displaybuses();
        for(Bus thebus:buses){
          tableModel.addRow(new Object[]{
                thebus.getPlate_nbr(),
              thebus.getBus_name(),
           thebus.getDriver(),
          thebus.getBus_company(),
          thebus.getNbr_passengers()
               
            });
        }
        }
