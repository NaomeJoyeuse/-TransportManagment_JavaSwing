    private void busTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_busTableMouseClicked

        int row=busTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(this, "select record from this table to delete or to update");
        }else {
            String plate_nbr = busTable.getValueAt(row, 0).toString();
            String bus_name = busTable.getValueAt(row, 1).toString();
            String driver = busTable.getValueAt(row, 2).toString();
            String bus_company = busTable.getValueAt(row, 3).toString();
            String passenger = busTable.getValueAt(row, 4).toString();
            plateBOX.setText(plate_nbr);
           // nameBOX.setText(bus_name);
            //driverBOX.setText(driver);
            //companyComboBox.setSelectedItem(bus_company);
            passengerBOX.setText(passenger);
            DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) companyComboBox.getModel();
            model.setSelectedItem(bus_company);
        }
    }//GEN-LAST:event_busTableMouseClicked

